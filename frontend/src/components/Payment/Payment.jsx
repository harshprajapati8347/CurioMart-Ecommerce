import React, { useState, useEffect } from "react";
import { useNavigate, useLocation } from "react-router-dom";
import styles from "../../styles/styles";
import {
  CardNumberElement,
  CardCvcElement,
  CardExpiryElement,
  useStripe,
  useElements,
} from "@stripe/react-stripe-js";
import { useSelector } from "react-redux";
import axios from "axios";
import { toast } from "react-toastify";
import { RxCross1 } from "react-icons/rx";

const Payment = () => {
  const [open, setOpen] = useState(false);
  const { user } = useSelector((state) => state.user);
  const navigate = useNavigate();
  const location = useLocation();
  const stripe = useStripe();
  const elements = useElements();

  const client_secret = location.state?.client_secret;
  const orders = location.state?.orders || [];

  useEffect(() => {
    if (!client_secret) {
      navigate("/checkout");
    }
  }, [client_secret, navigate]);

  const paymentHandler = async (e) => {
    e.preventDefault();
    try {
      if (!stripe || !elements) return;
      const result = await stripe.confirmCardPayment(client_secret, {
        payment_method: {
          card: elements.getElement(CardNumberElement),
        },
      });

      if (result.error) {
        toast.error(result.error.message);
      } else {
        if (result.paymentIntent.status === "succeeded") {
          // Fast-path verification: just redirect. Webhook handles DB update.
          toast.success("Order successful!");
          localStorage.setItem("cartItems", JSON.stringify([]));
          navigate("/order/success");
          window.location.reload();
        }
      }
    } catch (error) {
      toast.error(error.message || "Payment failed");
    }
  };

  const cashOnDeliveryHandler = async (e) => {
    e.preventDefault();
    // For COD, we would ideally call an endpoint to update the existing PENDING orders
    // to "Processing" and paymentStatus to "COD".
    // For now, redirecting to success as order is already saved.
    toast.success("Order successful!");
    localStorage.setItem("cartItems", JSON.stringify([]));
    navigate("/order/success");
    window.location.reload();
  };

  return (
    <div className="w-full flex flex-col items-center py-8">
      <div className="w-[90%] 1000px:w-[70%] block 800px:flex">
        <div className="w-full 800px:w-[65%]">
          <PaymentInfo
            user={user}
            open={open}
            setOpen={setOpen}
            paymentHandler={paymentHandler}
            cashOnDeliveryHandler={cashOnDeliveryHandler}
          />
        </div>
        <div className="w-full 800px:w-[35%] 800px:mt-0 mt-8">
          <CartData orders={orders} />
        </div>
      </div>
    </div>
  );
};

