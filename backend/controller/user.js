// update user avatar
router.put(
  "/update-avatar",
  isAuthenticated,
  upload.single("image"),
  catchAsyncErrors(async (req, res, next) => {
    try {
      const existsUser = await User.findById(req.user.id);

      if (!req.file) {
        return next(new ErrorHandler("Please upload an avatar image!", 400));
      }

      const uploadResult = await uploadOnCloudinary(req.file.path, {
        folder: "curiomart-server/users",
      });

      if (!uploadResult) {
        return next(
          new ErrorHandler("Avatar upload failed. Please try again.", 500)
        );
      }

      // only delete the old avatar once the new one is safely uploaded
      if (existsUser.avatar?.public_id) {
        await deleteFromCloudinary(existsUser.avatar.public_id);
      }

      const avatar = {
        public_id: uploadResult.public_id,
        url: uploadResult.secure_url,
      };

      const user = await User.findByIdAndUpdate(req.user.id, {
        avatar,
      });

      res.status(200).json({
        success: true,
        user,
      });
    } catch (error) {
      return next(new ErrorHandler(error.message, 500));
    }
  })
);

// update user addresses
router.put(
  "/update-user-addresses",
  isAuthenticated,
  catchAsyncErrors(async (req, res, next) => {
    try {
      const user = await User.findById(req.user.id);

      const sameTypeAddress = user.addresses.find(
        (address) => address.addressType === req.body.addressType
      );
      if (sameTypeAddress) {
        return next(
          new ErrorHandler(`${req.body.addressType} address already exists`)
        );
      }

      const existsAddress = user.addresses.find(
        (address) => address._id === req.body._id
      );

      if (existsAddress) {
        Object.assign(existsAddress, req.body);
      } else {
        // add the new address to the array
        user.addresses.push(req.body);
      }

      await user.save();

      res.status(200).json({
        success: true,
        user,
      });
    } catch (error) {
      return next(new ErrorHandler(error.message, 500));
    }
  })
);

